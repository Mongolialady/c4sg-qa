# T-C5 Contact form submits. The form submits only when valid information is entered
class TestPublicUser(unittest.TestCase):
    def setUp(self):
        self.driver = webdriver.Firefox()
        self.driver.implicitly_wait(30)

    # valid input
    def test_valid_name(self):
        self.driver.get("http://dev.code4socialgood.org/contact")
        element = self.driver.find_element_by_id("name")
        element.clear()        
        element.send_keys("abc")
        element = self.driver.find_element_by_id("name-label")
        self.assertIn("3/120", element.get_attribute("innerHTML"))
        
        element = self.driver.find_element_by_id("email")
        element.clear()        
        element.send_keys("abc@hotmail.com")
        element = self.driver.find_element_by_id("comment")
        element.clear()        
        element.send_keys("abc")
        self.driver.find_element_by_id("submit_btn").click()        
    
